export type PersonalAccessToken = {
    uuid: string;
    createdAt: Date;
    lastUsedAt: Date | null;
    rotatedAt: Date | null;
    expiresAt: Date | null;
    description: string;
};

export type PersonalAccessTokenWithToken = PersonalAccessToken & {
    token: string;
};

export type CreatePersonalAccessToken = Pick<
    PersonalAccessToken,
    'expiresAt' | 'description'
> & { autoGenerated: boolean };
