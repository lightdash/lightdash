// Regenerates src/generated/sdkVersion.ts from package.json before builds so
// published bundles always report their real version.
import { readFileSync, writeFileSync, mkdirSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = join(dirname(fileURLToPath(import.meta.url)), '..');
const { version } = JSON.parse(
    readFileSync(join(root, 'package.json'), 'utf-8'),
);
const outPath = join(root, 'src', 'generated', 'sdkVersion.ts');
mkdirSync(dirname(outPath), { recursive: true });
writeFileSync(
    outPath,
    `// Generated by scripts/generateSdkVersion.mjs (prebuild) — do not edit.\nexport const SDK_VERSION = '${version}';\n`,
);
