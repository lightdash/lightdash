import { ApiClient } from '../helpers/api-client';

const apiUrl = '/api/v1';

describe('Personal access tokens endpoints', () => {
    describe('POST /user/me/personal-access-tokens', () => {
        it('returns 401 when the request is not authenticated', async () => {
            const anonymous = new ApiClient();
            const resp = await anonymous.post(
                `${apiUrl}/user/me/personal-access-tokens`,
                {
                    description: 'x',
                    expiresAt: null,
                    autoGenerated: false,
                },
                { failOnStatusCode: false },
            );
            expect(resp.status).toBe(401);
        });

        it('returns 401 for an invalid ApiKey authorization header', async () => {
            const anonymous = new ApiClient();
            const resp = await anonymous.post(
                `${apiUrl}/user/me/personal-access-tokens`,
                {
                    description: 'x',
                    expiresAt: null,
                    autoGenerated: false,
                },
                {
                    headers: { Authorization: 'ApiKey nope' },
                    failOnStatusCode: false,
                },
            );
            expect(resp.status).toBe(401);
        });
    });
});
