/**
 * Create a project.
 * Equivalent to cy.createProject(projectName, warehouseConfig)
 */
export async function createProject(
    request: APIRequestContext,
    projectName: string,
    warehouseConfig: CreateWarehouseCredentials,
): Promise<string> {
    const response = await request.post('api/v1/org/projects', {
        headers: { 'Content-type': 'application/json' },
        data: {
            name: projectName,
            type: 'DEFAULT',
            dbtConnection: {
                target: '',
                environment: [],
                type: 'dbt',
                project_dir: process.env.DBT_PROJECT_DIR || '',
            },
            dbtVersion: 'v1.7',
            warehouseConnection: warehouseConfig || {
                host: process.env.PGHOST || 'localhost',
                user: 'postgres',
                password: process.env.PGPASSWORD || 'password',
                dbname: 'postgres',
                searchPath: '',
                role: '',
                sshTunnelHost: '',
                sshTunnelUser: '',
                schema: 'jaffle',
                port: 5432,
                keepalivesIdle: 0,
                sslmode: 'disable',
                sshTunnelPort: 22,
                requireUserCredentials: false,
                type: 'postgres',
            },
        },
    });
    expect(response.status()).toBe(200);
    const body = await response.json();
    return body.results.project.projectUuid;
}

/**
 * Create a space in a project.
 * Equivalent to cy.createSpace(projectUuid, spaceName)
 */
export async function createSpace(
    request: APIRequestContext,
    projectUuid: string,
    spaceName: string,
): Promise<string> {
    const response = await request.post(
        `api/v1/projects/${projectUuid}/spaces/`,
        {
            headers: { 'Content-type': 'application/json' },
            data: { name: spaceName },
        },
    );
    expect(response.status()).toBe(200);
    const body = await response.json();
    return body.results.uuid;
}

/**
 * Create a chart in a space.
 * Equivalent to cy.createChartInSpace(projectUuid, body)
 */
export async function createChartInSpace(
    request: APIRequestContext,
    projectUuid: string,
    body: CreateChartInSpace,
): Promise<SavedChart> {
    const response = await request.post(
        `api/v1/projects/${projectUuid}/saved`,
        { data: body },
    );
    expect(response.status()).toBe(200);
    const responseBody = await response.json();
    return responseBody.results;
}
